# CALCULATER QUESTION..........
num1=int(input("enter any number"))
num2=int(input("enter any number"))
character=input("enter any character")
if character=="+":
    print(num1+num2)
elif character=="-":
    print(num1-num2)
elif character=="*":
    print(num1*num2)
elif character=="%":
    print(num1%num2)
elif character=="/":
    print(num1/num2)
elif character=="//":
    print(num1//num2)
